package Flood;

public class Coordenada {
	private int iX;
	private int iY;

	public Coordenada(int iX, int iY) {
		this.iX = iX;
		this.iY = iY;
	}

	public int getX() {
		return this.iX;
	}
	public int getY() {
		return this.iY;
	}
	public void setX(int iX) {
		this.iX = iX;
	}

	public void setY(int iY) {
		this.iY = iY;
	}
};